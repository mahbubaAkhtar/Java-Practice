
package beginnerjava;


public class WhileLoop {
    public static void main(String[] args)
    {
        int i=0, j=1;
        while(i<=100)
        {
            System.out.println(i+" Stay home stay safe");
            i++;
        }
        while(j<=100)
        {
            System.out.println(j);
            j=j+2;
        }
    }
    
    
}
