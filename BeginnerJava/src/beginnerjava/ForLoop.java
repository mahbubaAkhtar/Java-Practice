
package beginnerjava;

public class ForLoop {
    public static void main(String[] args)
    {
        for(int i=0;i<=10;i++)
        {
            System.out.println(i+   " I hate you Sabu");
        }
        
        for(int j=0;j<=10;j++)
        {
            System.out.println(j);
            System.out.println("\n");
        }
        
        
        for(int k=10;k>=0;k--)
        {
            System.out.println(k);
        }
    }
}
