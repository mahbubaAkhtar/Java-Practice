
package beginnerjava;
public class AssignmentDemo {
    public static void main(String[] args)
    {
        int x=5,y=3;
        x+=y; // x = x + y=8
        System.out.println("x ="+x);
        x-=y; // x = x - y=5
        System.out.println("x ="+x);
        x*=y; // x = x * y=15
        System.out.println("x ="+x);
        x/=y; // x = x / y=5
        System.out.println("x ="+x);
        x%=y; // x = x % y =2
        System.out.println("x ="+x);
       
    
        
    }
    
}
